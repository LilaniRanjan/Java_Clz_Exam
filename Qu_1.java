package Exam_Java;

public class Qu_1 {

    public static void main(String[] args) {
        int num1= 20;
        int num2= 10;

        System.out.println(num1 + " + " + num2 + " = " +
                (num1 + num2));

        System.out.println(num1 + " - " + num2 + " = " +
                (num1 - num2));

        System.out.println(num1 + " x " + num2 + " = " +
                (num1 * num2));

        System.out.println(num1 + " / " + num2 + " = " +
                (num1 / num2));

        System.out.println(num1 + " % " + num2 + " = " +
                (num1 % num2));

    }
}
